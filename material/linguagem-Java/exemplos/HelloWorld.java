// Exemplo de programa estilo "Hello World" na linguagem Java

// Classe, com nível de acesso público, HelloWorld
public class HelloWorld {

  /* Método principal:
      public: É um modificador de acesso. Indica que este método pode ser chamado para outros objetos de outros tipos.
      static: Indica que o método está associado com a classe, não uma instância específica desta classe. Isto significa que você pode chamar sem criar um objeto da classe.
      void: Indica o tipo de retorno desse método. No caso, não há retorno. Outros valores poderiam ser int, short, long, float, chat etc.
  */
  public static void main(String[] args) {
    // Imprime mensagem
    System.out.println("Se a vida e um deserto, a minha e o Saara. \n");
  }
}
